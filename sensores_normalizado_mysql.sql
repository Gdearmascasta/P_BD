CREATE DATABASE db_sensores;

use db_sensores;
SET FOREIGN_KEY_CHECKS = 0;

-- TABLA: encargado
-- Descripción: Persona responsable de supervisar las mediciones

CREATE TABLE encargado (
    id_encargado  INT          AUTO_INCREMENT PRIMARY KEY,
    nombre        VARCHAR(100) NOT NULL,
    cc            VARCHAR(20)  NOT NULL UNIQUE,  -- Cédula de ciudadanía
    edad          TINYINT      NOT NULL
);


-- TABLA: zona
-- Descripción: Ubicación física donde se tomaron las mediciones

CREATE TABLE zona (
    id_zona          SMALLINT     AUTO_INCREMENT PRIMARY KEY,
    nombre_zona      VARCHAR(100) NOT NULL,
    temp_ambiente    DECIMAL(4,1),   -- Temperatura ambiental de referencia (°C)
    humedad_ambiente DECIMAL(4,1)    -- Humedad ambiental de referencia (%)
);


-- TABLA: sensor
-- Descripción: Dispositivo físico usado para medir

CREATE TABLE sensor (
    codigo_sensor  VARCHAR(10)  PRIMARY KEY,
    nombre_sensor  VARCHAR(100) NOT NULL,
    modelo         VARCHAR(20)  NOT NULL
);


-- TABLA: medida
-- Descripción: Registro individual de una lectura de sensor
-- Relaciones:
--   id_zona      -> zona(id_zona)         1:N
--   codigo_sensor-> sensor(codigo_sensor) 1:N
--   id_encargado -> encargado(id_encargado) 1:N

CREATE TABLE medida (
    id_medicion    INT          AUTO_INCREMENT PRIMARY KEY,
    fecha           DATE         NOT NULL,
    hora           TIME         NOT NULL,
    valor_temperatura DECIMAL(4,1) NOT NULL,   -- Temperatura medida (°C)
    valor_humedad  DECIMAL(4,1) NOT NULL,       -- Humedad relativa medida (%)
    codigo_sensor  VARCHAR(10)  NOT NULL,
    id_zona        SMALLINT     NOT NULL,
    id_encargado   INT          NOT NULL,
    CONSTRAINT fk_med_sensor    FOREIGN KEY (codigo_sensor) REFERENCES sensor(codigo_sensor),
    CONSTRAINT fk_med_zona      FOREIGN KEY (id_zona)       REFERENCES zona(id_zona),
    CONSTRAINT fk_med_encargado FOREIGN KEY (id_encargado)  REFERENCES encargado(id_encargado)
);

CREATE INDEX idx_med_zona     ON medida(id_zona);
CREATE INDEX idx_med_sensor   ON medida(codigo_sensor);
CREATE INDEX idx_med_fecha    ON medida(fecha);
CREATE INDEX idx_med_encargado ON medida(id_encargado);


